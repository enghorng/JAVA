    private void txtClassCodeCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtClassCodeCaretUpdate
        try {
            lblAlert.setText("OK");
            lblAlert.setForeground(Color.black);
            clsCon.setRs(clsCon.getStmt().executeQuery("select * from tblClass where ClassID='"+txtClassCode.getText()+"'"));
            clsCon.getRs().first();

            if(txtClassCode.getText().equals(""))
            lblAlert.setText("Null");
            if(txtClassCode.getText().equals("C00")||txtClassCode.getText().equals("C0")||txtClassCode.getText().equals("C")){
                lblAlert.setForeground(Color.red);
                lblAlert.setText("Cannot");
            }
            if(clsCon.getRs().getString("ClassID").equals(txtClassCode.getText())){
                lblAlert.setForeground(Color.red);
                lblAlert.setText("This ID is already exist!!");
            }
        } catch (SQLException ex) {

        }
    }//GEN-LAST:event_txtClassCodeCaretUpdate

