    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int answer;
        String sql;
        if(lblAlert.getText().equals("This ID is already exist!!")){
            answer = JOptionPane.showConfirmDialog(this, "Do you want to delete?", "Delete", JOptionPane.YES_NO_OPTION);
            if(answer==JOptionPane.YES_OPTION){

                sql = "delete from tblClass where ClassID='"+ txtClassCode.getText() +"'";
                try{
                    if(lblAlert.getText().equals("Null")||lblAlert.getText().equals("Cannot")){
                        txtClassCode.requestFocusInWindow();
                        return;
                    }
                    if(lblAlert.getText().equals("This ID is already exist!!")){
                        clsCon.getStmt().executeUpdate(sql);
                        lblAlert1.setText("");
                        JOptionPane.showMessageDialog(this, "Delete is Successful");
                        clear();
                    }
                    showdate();
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(this, "Cannot Delete");
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

