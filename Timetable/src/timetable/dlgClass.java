    public void btnAdd(){
        String sql=null;
        if(lblAlert.getText().equals("OK"))
            sql = "insert into tblClass(ClassID,ClassName) values('"+ txtClassCode.getText() +"','"+ txtClassName.getText() +"')";
        else if(lblAlert.getText().equals("This ID is already exist!!"))
            sql = "update tblClass set ClassName='"+ txtClassName.getText() +"' where ClassID='"+ txtClassCode.getText() +"'";
        try{
            if(lblAlert.getText().equals("Null")||lblAlert.getText().equals("Cannot")){
                txtClassCode.requestFocusInWindow();
                return;
            }
            if(txtClassName.getText().equals("")){
                txtClassName.requestFocusInWindow();
                lblAlert1.setText("Null");
                lblAlert1.setForeground(Color.red);
                return;
            }
            if(lblAlert.getText().equals("OK")){
                clsCon.getStmt().executeUpdate(sql);
                lblAlert1.setText("");
                JOptionPane.showMessageDialog(this, "A new record was saved.");
                clear();
            }
            if(lblAlert.getText().equals("This ID is already exist!!")){
                clsCon.getStmt().executeUpdate(sql);
                lblAlert1.setText("");
                JOptionPane.showMessageDialog(this, "Update is successful.");
                clear();
            }
            showdate();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "This proccess is fail.");
        }
    }
    
